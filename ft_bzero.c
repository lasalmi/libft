#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*arr;
	int		i;

	if (s == NULL)
		return ;
	arr = s;
	i = 0;
	while (n > 0)
	{
		arr[i++] = '\0';
		n--;
	}
}
